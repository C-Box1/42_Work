#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	size_t	trim_len;
	char	*trim_str;
	size_t	start;
	size_t	end;

	if (!s1)
		return (NULL);
	if (!set)
		return (ft_strdup(s1));
	if (s1[0] == '\0')
		return (ft_strdup(""));
	start = 0;
	end = ft_strlen(s1);
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	while (end > start && ft_strchr(set, s1[end - 1]))
		end--;
	trim_len = end - start;
	trim_str = (char *)malloc(sizeof(char) * (trim_len + 1));
	if (trim_str == NULL)
		return (NULL);
	ft_memcpy(trim_str, s1 + start, trim_len);
	trim_str[trim_len] = '\0';
	return (trim_str);
}
